public class Komentar {
    public static void main(String[] args) {
        System.out.println(sum(10, 10));
    }
    /**
     * Method untuk menjumlahkan bil1 dan bil2
     * @param bil1 angka pertama
     * @param bil2 angka kedua
     * @return hasil penjumlahan bil1 dan bil2
     */
    static int sum(int bil1, int bil2) {
        // hitung bil1 ditambah bil2
        return bil1 + bil2;
    }
}
