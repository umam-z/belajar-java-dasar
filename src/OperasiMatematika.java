public class OperasiMatematika {
    public static void main(String[] args) {
        byte x = 100;
        byte y = 10;

        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x / y);
        System.out.println(x * y);
        System.out.println(x % y);

        short z = 100;
        
        z += 10;
        System.out.println(z);

        z /= 10;
        System.out.println(z);

        z *= 10;
        System.out.println(z);

        z -= 10;
        System.out.println(z);

        int d = 100;

        d++;
        System.out.println(d);

        d--;
        System.out.println(d);
    }
}
