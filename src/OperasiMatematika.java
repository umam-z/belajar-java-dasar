public class OperasiMatematika{
    public static void main(String[] args){
        int a = 10;
        int b = 20;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

        int c = 100;

        c += 10;
        System.out.println(c);

        c -= 10;
        System.out.println(c);

        c *= 10;
        System.out.println(c);

        int d = 100;

        d++;
        System.out.println(d);

        d--;
        System.out.println(d);
    }
}
