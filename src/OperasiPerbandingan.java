public class OperasiPerbandingan {
    public static void main(String[] args) {
        byte x = 100;
        byte y = 100;

        System.out.println(x > y);
        System.out.println(x < y);
        System.out.println(x <= y);
        System.out.println(x >= y);
        System.out.println(x != y);
        System.out.println(x == y);
    }
}
